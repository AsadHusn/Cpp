#include<stdio.h>
int main()
{
	char c;
	printf("Enter an alphabet:");
	scanf("%c",&c);
	switch(c)
	{
		case 'a':
			case'A':
				case 'e':
					case 'E':
						case 'i':
							case 'I':
								case 'o':
									case 'O':
										case 'u':
											case 'U':
												printf("You entered a vowel");
												break;
												default:
													printf("You Entered a consonant");
	}
}
